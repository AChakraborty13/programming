/*

  + : addition
  - : subtraction
  * : multiplication
  / : division
  % : remainder
 ** : power

 */

assert  1  + 2 == 3 // addition
assert  4  - 3 == 1 // subtraction
assert  3  * 5 == 15 // multiplication
assert  3  / 2 == 1.5 // division
assert 10  % 3 == 1 // remainder
assert  2 ** 3 == 8 // power

